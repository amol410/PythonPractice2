class Students:
    def __init__(delf,name,roll,marks):
        delf.name=name
        delf.roll=roll
        delf.marks=marks

    def m1(kelf):
        print('name:',kelf.name)
        print('roll:',kelf.roll)
        print('marks:',kelf.marks)

s1=Students('robineha',99,100)
